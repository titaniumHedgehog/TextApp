#include "GoCommand.h"

#include <algorithm>
#include <iostream>

//--------------------------------------------------------------------------------------------------------------------------------

GoCommand::GoCommand() : Command("Go")
{

}

//--------------------------------------------------------------------------------------------------------------------------------

void	GoCommand::Execute(std::vector<std::string> const& Params)
{
	std::cout << "Go for it: ";
	std::for_each(Params.begin(), Params.end(), [](std::string param) {
		std::cout << param << " ";
		});

	std::cout << "\n";
}

