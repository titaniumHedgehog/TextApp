#include "Command.h"

#include <string.h>

//--------------------------------------------------------------------------------------------------------------------------------

Command::Command(char const* TheVerb)
{
	M_Verb = TheVerb;
}

//--------------------------------------------------------------------------------------------------------------------------------

Command::~Command()
{

}

//--------------------------------------------------------------------------------------------------------------------------------

bool	Command::IsVerb(char const* VerbText) const
{
	return _stricmp(VerbText, M_Verb.c_str()) == 0;
}


//--------------------------------------------------------------------------------------------------------------------------------

std::string const& Command::GetVerb() const
{
	return M_Verb;
}

